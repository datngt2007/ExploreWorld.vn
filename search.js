let product_div = document.getElementById("main");


function loadProducts(products) {
  for (let i = 0; i < products.length; i++) {
    let output = ` 
    <div id="main" style="height: auto; display: flex; flex-wrap: wrap;">
        <div class="ma" style=" width: 320px; text-align: center; border: 3px solid black; margin: 29px; ">
            <img src="${products[i].imageLink}" alt="" style="width: 315px; height: 300px;" id="img"/>
            <h2 id="name_product" style="font-size: 30px; height: 170px;">${products[i].location}</h2>
            <div class="bottom">
                <button class="add-to-cart"
                    style="font-size: 20px; width : 100px; height: 50px; margin-bottom: 10px;">See More</button>
                <button class="fav-button" style="border: none; width: 50px; height: 50px; margin-bottom: 20px; background-color: white; padding-left: 80px; font-size: 30px;"><i class="fa-solid fa-heart" style="color: #bfbfbf;"></i></button>
            </div>
        </div>
    </div>
    `;
    product_div.innerHTML += output;
  }
  
  const favButtons = document.querySelectorAll('.fav-button');
  favButtons.forEach((button, index) => {
    button.addEventListener('click', () => handleFavorite(index));
  });
}

function handleFavorite(index) {
  const heartIcon = document.querySelectorAll('.fav-button i')[index];
  if (heartIcon.classList.contains('far')) {
    heartIcon.classList.remove('far');
    heartIcon.classList.add('fas');
    heartIcon.style.color = 'red';
  } else {
    heartIcon.classList.remove('fas');
    heartIcon.classList.add('far');
    heartIcon.style.color = '#bfbfbf';
  }
}

